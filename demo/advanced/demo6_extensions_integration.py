class CloudIntegration:
    """
    Demonstrates cloud provider extensions for deployment automation
    """

    def generate_kubernetes_manifest(self, app_config: DeploymentConfig) -> str:
        """
        Generate Kubernetes deployment manifests

        üéØ Demo Prompt: "Deploy my Flask app to Kubernetes with auto-scaling"
        """
        k8s_manifest = f"""
# Generated by Copilot Cloud Extension
apiVersion: apps/v1
kind: Deployment
metadata:
  name: {app_config.app_name}
  namespace: {app_config.environment}
  labels:
    app: {app_config.app_name}
    environment: {app_config.environment}
spec:
  replicas: {app_config.replicas}
  selector:
    matchLabels:
      app: {app_config.app_name}
  template:
    metadata:
      labels:
        app: {app_config.app_name}
    spec:
      containers:
      - name: {app_config.app_name}
        image: {app_config.image_tag}
        ports:
        - containerPort: 5000
        env:
        - name: ENVIRONMENT
          value: "{app_config.environment}"
        resources:
          requests:
            memory: "256Mi"
            cpu: "250m"
          limits:
            memory: "512Mi"
            cpu: "500m"
        livenessProbe:
          httpGet:
            path: /health
            port: 5000
          initialDelaySeconds: 30
          periodSeconds: 10
        readinessProbe:
          httpGet:
            path: /ready
            port: 5000
          initialDelaySeconds: 5
          periodSeconds: 5
---
apiVersion: v1
kind: Service
metadata:
  name: {app_config.app_name}-service
  namespace: {app_config.environment}
spec:
  selector:
    app: {app_config.app_name}
  ports:
  - protocol: TCP
    port: 80
    targetPort: 5000
  type: LoadBalancer
---
apiVersion: autoscaling/v2
kind: HorizontalPodAutoscaler
metadata:
  name: {app_config.app_name}-hpa
  namespace: {app_config.environment}
spec:
  scaleTargetRef:
    apiVersion: apps/v1
    kind: Deployment
    name: {app_config.app_name}
  minReplicas: 2
  maxReplicas: 10
  metrics:
  - type: Resource
    resource:
      name: cpu
      target:
        type: Utilization
        averageUtilization: 70
"""
        return k8s_manifest

